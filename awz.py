import time
#function defination
def insertionsort(arr):
    for i in range(1,len(arr)):
        key=arr[i]
        j=i-1
        while j>=0 and key<arr[j]:
          arr[j+1]=arr[j]
          j=j-1
#place key at after the element just smaller than it
          arr[j+1]=key
#driver code
arr=[9,8,7,6,5,4]
start=time.time()
print("the orginial array is:")
for i in range(len(arr)):
    print(arr[i])
